// Custom Table component for your use case
type Column<T> = {
    header: string
    accessor: keyof T
}

type CustomTableProps<T> = {
    data: T[]
    columns: Column<T>[]
    className?: string
}

export function CustomTable<T>({ data, columns, className }: CustomTableProps<T>) {
    return (
        <div className={cn("bg-transparent text-card-foreground shadow border border-dashed border-border rounded mx-4", className)}>
            <Table>
                <TableHeader>
                    <TableRow>
                        {columns.map((col, i) => (
                            <TableHead key={i}>
                                {col.header}
                            </TableHead>
                        ))}
                    </TableRow>
                </TableHeader>
                <TableBody>
                    {data.map((row, i) => (
                        <TableRow key={i}>
                            {columns.map((col, j) => (
                                <TableCell key={j}>
                                    {String(row[col.accessor])}
                                </TableCell>
                            ))}
                        </TableRow>
                    ))}
                </TableBody>
            </Table>
        </div>
    )
}

export {
  Table,
  TableHeader,
  TableBody,
  TableFooter,
  TableHead,
  TableRow,
  TableCell,
  TableCaption,
}
